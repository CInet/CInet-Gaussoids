# Read the formula for oriented 3-gaussoids in a 3-face 123
# and preprocess it, so that we can instantiate it into a
# clauses with a single map through a lookup table.
my @o3 = map { [ split /\|/ ] } map { s/~/-/gr }
    map { chomp; $_ } <DATA>;

sub oriented_axioms {
    my $cube = shift;
    my @axioms;
    for my $ijK ($cube->squares) {
        my $idx = $cube->pack($ijK);
        my ($x, $y) = (2*$idx - 1, 2*$idx);
        push @axioms, [ -$x, $y ];
    }
    for my $ijkL ($cube->faces(3)) {
        my ($i, $j, $k) = $ijkL->[0]->@*;
        my @L = $ijkL->[1]->@*;
        my %lookup = (
            q[a12t'1]  => -1+2*$cube->pack([ [$i,$j], [@L] ]),    q[a12t'2]  => 2*$cube->pack([ [$i,$j], [@L] ]),
            q[a12t3'1] => -1+2*$cube->pack([ [$i,$j], [$k,@L] ]), q[a12t3'2] => 2*$cube->pack([ [$i,$j], [$k,@L] ]),
            q[a13t'1]  => -1+2*$cube->pack([ [$i,$k], [@L] ]),    q[a13t'2]  => 2*$cube->pack([ [$i,$k], [@L] ]),
            q[a13t2'1] => -1+2*$cube->pack([ [$i,$k], [$j,@L] ]), q[a13t2'2] => 2*$cube->pack([ [$i,$k], [$j,@L] ]),
            q[a23t'1]  => -1+2*$cube->pack([ [$j,$k], [@L] ]),    q[a23t'2]  => 2*$cube->pack([ [$j,$k], [@L] ]),
            q[a23t1'1] => -1+2*$cube->pack([ [$j,$k], [$i,@L] ]), q[a23t1'2] => 2*$cube->pack([ [$j,$k], [$i,@L] ]),
        );
        for my $o (@o3) {
            push @axioms, [ map { s/(a.+)$/$lookup{$1}/er } @$o ];
        }
    }
    \@axioms
}

use CInet::Hash::FaceKey;
tie my %SOLVERS, 'CInet::Hash::FaceKey';
sub oriented_solver {
    my $cube = shift;
    $SOLVERS{[ $cube->set, [] ]} //=
        CInet::ManySAT::Incremental->new->read(oriented_axioms $cube)
}

sub orientations :Export(:DEFAULT) {
    my $A = shift;
    my $cube = $A->cube;
    my $solver = oriented_solver($cube);
    my @witt;
    for my $ijK ($cube->squares) {
        my $idx = $cube->pack($ijK);
        my ($x, $y) = (2*$idx - 1, 2*$idx);
        push @witt, +$x if $A->cival($ijK) eq 0;
        push @witt, -$x if $A->cival($ijK) eq 1;
    }
    ($solver, \@witt)
}

sub is_orientable :Export(:DEFAULT) {
    my $A = shift;
    my $cube = $A->cube;
    my $solver = oriented_solver($cube);
    my @witt;
    for my $ijK ($cube->squares) {
        my $idx = $cube->pack($ijK);
        my ($x, $y) = (2*$idx - 1, 2*$idx);
        push @witt, +$x if $A->cival($ijK) eq 0;
        push @witt, -$x if $A->cival($ijK) eq 1;
    }
    $solver->solve(\@witt)
}

sub orientable_completion :Export(:DEFAULT) {
    my $A = shift;
    my $cube = $A->cube;
    my $solver = oriented_solver($cube);
    my @witt;
    for my $ijK ($cube->squares) {
        my $idx = $cube->pack($ijK);
        my ($x, $y) = (2*$idx - 1, 2*$idx);
        $solver->read([ +$x ]) if $A->cival($ijK) eq 0;
    }

    my $B = $A->clone;
    for my $ijK ($cube->squares) {
        my $idx = $cube->pack($ijK);
        my ($x, $y) = (2*$idx - 1, 2*$idx);
        my $sat = $solver->solve([ -$x ]);
        # Not satisfiable means $ijK is implied:
        $B->cival($ijK) = 0 if not $sat;
    }
    $B
}

